        /// <summary>
        /// 目标版本与当前版本做比较,相同则返回0 ，目的版本比源版本高返回1，否则返回-1
        /// </summary>
        /// <param name="sourceVersion"></param>
        /// <param name="destVersion"></param>
        /// <returns></returns>
        public static int CompareVersion(string sourceVersion, string destVersion)
        {
            int result = -1;
            if (destVersion.Equals(sourceVersion))
            {
                result = 0;
            }
            else
            {
                var versionSourceInfo = sourceVersion.Split('.').Select(p=>Convert.ToInt32(p)).ToList();
                var versionDestInfo = destVersion.Split('.').Select(p => Convert.ToInt32(p)).ToList();
                for (int i = 0; i < versionSourceInfo.Count; i++)
                {
                    if (versionDestInfo[i] > versionSourceInfo[i])
                    {
                        result = 1;
                        break;
                    }
                }

              
            }
            return result;
        }

        /// <summary>
        /// List<T> 转换成DataSet
        /// </summary>
        /// <typeparam name="T">对象</typeparam>
        /// <param name="list">集合</param>
        /// <returns>DataSet</returns>
        public static DataSet ConvertToDataSet<T>(IList<T> list)
        {
            if (list == null || list.Count <= 0)
            {
                return null;
            }

            DataSet ds = new DataSet();
            DataTable dt = new DataTable(typeof(T).Name);
            DataColumn column;
            DataRow row;

            System.Reflection.PropertyInfo[] myPropertyInfo = typeof(T).GetProperties(System.Reflection.BindingFlags.Public | System.Reflection.BindingFlags.Instance);

            foreach (T t in list)
            {
                if (t == null)
                {
                    continue;
                }

                row = dt.NewRow();

                for (int i = 0, j = myPropertyInfo.Length; i < j; i++)
                {
                    System.Reflection.PropertyInfo pi = myPropertyInfo[i];

                    string name = pi.Name;

                    if (dt.Columns[name] == null)
                    {
                        column = new DataColumn(name, pi.PropertyType);
                        dt.Columns.Add(column);
                    }

                    row[name] = pi.GetValue(t, null);
                }

                dt.Rows.Add(row);
            }
            ds.Tables.Add(dt);
            return ds;
        }
